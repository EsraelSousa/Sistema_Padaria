public class Produtos {
    private long idProd;
    private String nomeProd;
    private String descricao; 
    private String qtdeProd; 
    private String valUnitProd;
    private String ValTotalProd;

    public long getIdProd() {
        return idProd;
    }

    public void setIdProd(long idProd) {
        this.idProd = idProd;
    }

    public String getNomeProd() {
        return nomeProd;
    }

    public void setNomeProd(String nomeProd) {
        this.nomeProd = nomeProd;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getQtdeProd() {
        return qtdeProd;
    }

    public void setQtdeProd(String qtdeProd) {
        this.qtdeProd = qtdeProd;
    }

    public String getValUnitProd() {
        return valUnitProd;
    }

    public void setValUnitProd(String valUnitProd) {
        this.valUnitProd = valUnitProd;
    }

    public String getValTotalProd() {
        return ValTotalProd;
    }

    public void setValTotalProd(String ValTotalProd) {
        this.ValTotalProd = ValTotalProd;
    }
    
    
            
}
